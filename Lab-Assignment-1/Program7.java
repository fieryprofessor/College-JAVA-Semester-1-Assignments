public class Program7{
    public static void main(String args[]){
        int x=5,y=6;
        System.out.println("Before swapping");
        System.out.println("x is= "+x+" and y is= "+y);
        int z;
        z=x;
        x=y;
        y=z;
         System.out.println("After swapping");
        System.out.println("x is= "+x+" and y is= "+y);
    }
}